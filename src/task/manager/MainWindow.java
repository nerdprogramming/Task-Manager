    private void lstTasksValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstTasksValueChanged
        // TODO add your handling code here
        int idx = lstTasks.getSelectedIndex();
        if(idx > -1) {
            Task curr = tasks.get(lstTasks.getSelectedIndex());
            //txtaDesc.setText(curr.getDesc());
            txtfName.setText(curr.getTitle());
            cmbPrio.setSelectedIndex(curr.getPriority().ordinal());
        } else {
            //txtaDesc.setText("");
            txtfName.setText("");
            cmbPrio.setSelectedIndex(0);
        }
        
    }//GEN-LAST:event_lstTasksValueChanged

    private void btnCompleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompleteActionPerformed
        // TODO add your handling code here:
        int idx = lstTasks.getSelectedIndex();
        
        if(idx > -1) {
            Task curr = tasks.get(idx);
            tasks.remove(curr);
            isDirty = true;
        }
        updateList();
        
    }//GEN-LAST:event_btnCompleteActionPerformed

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser("C:/tmp/");
        jfc.showSaveDialog(jMenu1);
        File file = jfc.getSelectedFile();
        try(BufferedWriter bw = new BufferedWriter(new FileWriter(file))) {
           for(Task t : tasks) {
               if(t.getTitle() != null && t.getTitle().length() > 0) {
                   bw.write(t.toCSV());
                   bw.newLine();
               }
           }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jMenuItem1ActionPerformed

