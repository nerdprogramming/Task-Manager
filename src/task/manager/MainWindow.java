    public void updateUI() {
        int idx = lstTasks.getSelectedIndex();
        if(idx > -1) {
            Task curr = tasks.get(idx);
            //txtaDesc.setText(curr.getDesc());
            txtfName.setText(curr.getTitle());
            cmbPrio.setSelectedIndex(curr.getPriority().ordinal());
            
        } 
        
        updateList();
    }
    
    public void updateTask() {
        int idx = lstTasks.getSelectedIndex();
        
        if(idx > -1) {
            Task curr = tasks.get(idx);
            curr.setPriority(Task.Prio.values()[cmbPrio.getSelectedIndex()]);
            curr.setTitle(txtfName.getText());
        } else {
            tasks.add(new Task(Task.Prio.values()[cmbPrio.getSelectedIndex()],txtfName.getText(),""));
            
        }
    }
